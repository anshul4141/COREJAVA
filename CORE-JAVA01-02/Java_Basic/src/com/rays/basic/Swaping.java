package com.rays.basic;

public class Swaping {

	public static void main(String[] args) {

		int a = 5;
		int b = 10;
		int c = 0;

		System.out.println("a = " + a);
		System.out.println("b = " + b);

		System.out.println("-----------");
		// swaping

		c = a; // c = 5;
		a = b; // a = 10;
		b = c; // b = 5;
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);


	}

}
