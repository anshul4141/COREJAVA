package com.rays.collection.streamApi;

public class Contestant {

	public String phoneNo;
	public String name;

	public Contestant(String phoneNo, String name) {

		this.phoneNo = phoneNo;
		this.name = name;

	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public String getName() {
		return name;
	}

}
